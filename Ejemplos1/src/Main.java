public class BooleanExamples {
    public static void main(String[] args) {
        // Ejemplo 1: Valor booleano verdadero
        boolean isSunny = true;
        System.out.println("Is it sunny? " + isSunny);

        // Ejemplo 2: Valor booleano falso
        boolean isRaining = false;
        System.out.println("Is it raining? " + isRaining);

        // Ejemplo 3: Uso de una condiciÃ³n para obtener un valor booleano
        int apples = 3;
        int oranges = 5;
        boolean hasMoreOranges = oranges > apples;
        System.out.println("Are there more oranges than apples? " + hasMoreOranges);
    }
}
